    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int fila = tabla.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        } else {
            String codigo = tabla.getValueAt(fila, 1).toString();
            String nombre = tabla.getValueAt(fila, 2).toString();
            String descripcion = tabla.getValueAt(fila, 3).toString();
            String precio = tabla.getValueAt(fila, 4).toString();
            String cant = tabla.getValueAt(fila, 5).toString();
            String id = tabla.getValueAt(fila, 0).toString();

            txtCodigo.setText(codigo);
            txtNombre.setText(nombre);
            txtDes.setText(descripcion);
            txtPrecio.setText(precio);
            txtCant.setText(cant);
            txtId.setText(id);

        }
    }//GEN-LAST:event_tablaMouseClicked

