    void agregar() {
        String codigo = txtCodigo.getText();
        String nombre = txtNombre.getText();
        String descripcion = txtDes.getText();
        Double precio = Double.parseDouble(txtPrecio.getText().toString());
        int cant = Integer.parseInt(txtCant.getText().toString());

        Object[] ob = new Object[6];
        ob[1] = codigo;
        ob[2] = nombre;
        ob[3] = descripcion;
        ob[4] = precio;

        prod.agregar(ob);

        ob[1] = tienda;
        ob[2] = codigo;
        ob[3] = cant;
        prod.AgregarInventario(ob);

    }

    void limpiarTabla() {
        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
            i = i - 1;
        }
    }

    private void LimpiarCampos() {
        txtCodigo.setText("");
        txtNombre.setText("");
        txtPrecio.setText("");
        txtCant.setText("");
        txtDes.setText("");

    }

    void actualizar() {
        String codigo = txtCodigo.getText();
        String nombre = txtNombre.getText();
        String descripcion = txtDes.getText();
        Double precio = Double.parseDouble(txtPrecio.getText().toString());
        int cant = Integer.parseInt(txtCant.getText().toString());
        int id = Integer.parseInt(txtId.getText());

        Object[] ob = new Object[6];
        ob[0] = codigo;
        ob[1] = nombre;
        ob[2] = descripcion;
        ob[3] = precio;

        prod.actualizar(ob);
        ob[0] = id;
        ob[1] = tienda;
        ob[2] = codigo;
        ob[3] = cant;
        prod.actualizarInventario(ob);

    }


